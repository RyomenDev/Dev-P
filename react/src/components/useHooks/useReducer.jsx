import { useReducer } from "react";

const reducer = (state, action) => {
  switch (action.type) {
    case "increment":
      return { count: state.count + 1 };
    case "decrement":
      return { count: state.count - 1 };
    case "reset":
      return { count: 0 };
    default:
      return state;
  }
};

export const useCustomReducer = () => {
  const [state, dispatch] = useReducer(reducer, { count: 0 });
  return { state, dispatch };
};
